package luthfi_14_maret.latihan;

public class cetakseratuskali {

        // while loop
    public static void main(String[] args) {
        int count = 0;
        while (count < 100) {
            System.out.println("Luthfi");
            count++;
        }

        System.out.println("========================================");
        System.out.println("========DO - WHILE LOOP=================");
        // do while loop

        int n = 0;
        do {
            System.out.println("luthfi");
            n++;
        } while (n < 100);

        System.out.println("========================================");
        System.out.println("========FOR - LOOP=================");

        //  FOR LOOP

        for (int K = 0; K < 100; K++) {
            System.out.println("LUTHFI");
        }
    }
}